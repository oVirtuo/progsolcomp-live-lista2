public class Exe14_FillVetor50 {

    public static void main(String[] args) throws Exception{
        int[] vetor = new int[50];
        int n = vetor.length;
        for (int i = 0; i < n - 1; i++) {
            vetor [i] = 0;
            System.out.print(vetor[i]+" ");           
        }
        

    }
}
